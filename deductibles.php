        <h1>How do deductibles, coinsurance and copays work?</h1>
          <p>When both you and your health insurance company pay part of your medical expense, it's called cost sharing. Deductibles, coinsurance and copays are all examples. Understanding how they work will help you know when and how much you have to pay for care.</p>

          <ul id="listItem"><br />
          <p>Deductible</p>
            <ul>
              <li>A deductible is the amount you pay for health care services before your health insurance begins to pay.</li>
              <p>Let's say your plan's deductible is $1,500. That means for most services, you'll pay 100 percent of your medical and pharmacy bills until the amount you pay reaches $1,500. After that, you share the cost with your plan by paying coinsurance and copays.</p>
              <p>Coinsurance</p>
              <li>Coinsurance is your share of the costs of a health care service. It's usually figured as a percentage of the amount we charge for services. You start paying coinsurance after you've paid your plan's deductible.</li>
            </ul><br />

          <P>Here's how it works. Lisa has allergies, so she sees a doctor regularly. She just paid her $1,500 deductible which is the required amount of the insurance plan that Lisa chose. Now Lisa's plan will cover 70 percent of the cost of her allergy shots. Lisa pays the other 30 percent, which is her coinsurance. If her treatment costs $150, her plan will pay $105, and she'll pay $45.</P>

          <P>If Lisa has a PPO plan, she has the option to see any doctor she wants. If she goes to an out-ofnetwork doctor, her plan will still share the cost, but her percentage of coinsurance will be higher. And, if the medical service she gets is more than what her plan would pay for an in-network doctor, she'll have to pay the difference.</P>

          <ul>
            <p>Copay</p>
            <li>A copay is a fixed amount you pay for a health care service, usually when you receive the service. The amount can vary by the type of service. You may also have a copay when you get a prescription filled.</li><br />
            <p>For example, a doctor's office visit might have a copay of $30. The copay for an emergency room visit will usually cost more, such as $250. For some services, you may have both a copay and coinsurance. Again, the amount that you are required to pay is dependent upon the insurance plan you chose.</p>
          </ul><br />
        </ul><br />

      <h1>What does this mean for me?</h1>
        <ul id="listItem">
          <p>When choosing a plan, think about how much you use your insurance and how much protection you want against unpredictable expenses. Then look at the plan's deductible, coinsurance and copays, and consider what works best for you. Here are a few things to consider.</p>
            <ul>
              <li>Deductible: A plan with a high deductible will have cheaper monthly payments. But you'll pay a lot upfront when you need care. You can also look for plans that cover some services before you pay your deductible.</li>
              <li>Coinsurance: Typically, the lower a plan's monthly payments, the more you'll pay in coinsurance.</li>
              <li>Copays: If you visit your doctor or pharmacy often, you might want to choose a plan that has a low copay for office visits and prescriptions.</li>
            </ul><br />
        </ul>

      <h1>Coinsurance Example</h1>
        <ul id="listItem">
          <p>Coinsurance is the percentage of costs of a covered health care service after you've paid your deductible.</p>
            <ul>
              <p>Let's say your health insurance plan's allowed amount for an office visit is $100 and your coinsurance is 20%.</p>
              <li>If you've paid your deductible: You pay 20% of $100, or $20. The insurance company pays the rest.</li>
              <li>If you haven't met your deductible: You pay the full allowed amount, $100.</li>
            </ul><br />
            <p>Example of coinsurance with high medical costs</p>
              <ul>
                <p>Let's say the following amounts apply to your particular plan and you need many treatments for a serious condition. Allowable costs are $12,000.</p>
                <li>Deductible: $3,000</li>
                <li>Coinsurance: 20%</li>
                <li>Out-of-pocket maximum: $6,850</li>
              </ul><br />
              <p>You must pay all of the first $3,000 (your deductible).</p>
              <p>You'll pay 20% of the remaining $9,000, or $1,800 (your coinsurance).</p>
              <p>Your total out-of-pocket costs would be $4,800 -your $3,000 deductible plus your $1,800 coinsurance.</p>
              <p>If your total out-of-pocket costs reach $6,850, you'd pay only that amount, including your deductible and coinsurance. The insurance company would pay for all covered services for the rest of your plan year. Generally speaking, plans with low monthly premiums have higher coinsurance, and plans with higher monthly premiums have lower coinsurance</p>
          </ul><br />
